package utilidades;

public class Personagem {
	private String nome;
	
	private byte forca;
	private byte destreza;
	private byte vitalidade;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public byte getForca() {
		return forca;
	}
	public void setForca(byte forca) {
		this.forca = forca;
	}
	public byte getDestreza() {
		return destreza;
	}
	public void setDestreza(byte destreza) {
		this.destreza = destreza;
	}
	public byte getVitalidade() {
		return vitalidade;
	}
	public void setVitalidade(byte vitalidade) {
		this.vitalidade = vitalidade;
	}
	
	
}
